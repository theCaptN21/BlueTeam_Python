#This is an example of typecasting an integer to be a float
Input = float(1)
Output = 1.0

#This is an example of typecasting an integer
Input = int(1.3)
Output = 1

#This is an example of creating a string value of an integer
Input = str(1)
Output = '1'

#This is an example of a boolean (almost everything will have a value of true)
Input = bool(1)
Output = True

#This is an example of returning the false value
Input = 1 and 0
Output = 0

#This is an example of showing an empty value
Input = not ""
Output = True


